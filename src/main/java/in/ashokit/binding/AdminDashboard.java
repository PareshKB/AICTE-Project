package in.ashokit.binding;

public class AdminDashboard {
	
	private Integer noOfSubjects;
	private Integer noOfCategories;
	private Integer totalAttempts;
	private Integer passedAttempts;
	public Integer getNoOfSubjects() {
		return noOfSubjects;
	}
	public void setNoOfSubjects(Integer noOfSubjects) {
		this.noOfSubjects = noOfSubjects;
	}
	public Integer getNoOfCategories() {
		return noOfCategories;
	}
	public void setNoOfCategories(Integer noOfCategories) {
		this.noOfCategories = noOfCategories;
	}
	public Integer getTotalAttempts() {
		return totalAttempts;
	}
	public void setTotalAttempts(Integer totalAttempts) {
		this.totalAttempts = totalAttempts;
	}
	public Integer getPassedAttempts() {
		return passedAttempts;
	}
	public void setPassedAttempts(Integer passedAttempts) {
		this.passedAttempts = passedAttempts;
	}
}
